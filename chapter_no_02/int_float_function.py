# int()
value_one = int(input("Enter first value: "))
value_two = int(input("Enter second number: "))
total = value_one + value_two
print(f"Sum: {str(total)}")


# float ()
value_one = float(input("Enter first value: "))
value_two = float(input("Enter second number: "))
total = value_one + value_two
print(f"Sum: {str(total)}")